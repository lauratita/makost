        // TODO add your handling code here:
        int i = tblTarif.getSelectedRow();
        if (i == -1) {

        } else {
            String id = tblTarif.getValueAt(i, 0).toString();
            String nama = tblTarif.getValueAt(i, 1).toString();
            String harga = tblTarif.getValueAt(i, 2).toString();
            int jmlh = 1;
            int hrg = Integer.parseInt(harga);
            int ttl = hrg * jmlh;

            Pembayaran.txtIdTarif.setText(id);
            Pembayaran.txtNamaTarif.setText(nama);
            Pembayaran.txtHargaTarif.setText(harga);
            Pembayaran.txtJumlahTarif.setText("1");
            Pembayaran.txtTotalTarif.setText(String.valueOf(ttl));

            dispose();
        }
