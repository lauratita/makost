    private void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection c = Koneksi.configDB();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM tbl_tarif_tambahan";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[4];
                o[0] = r.getString("id_tarif");
                o[1] = r.getString("nama_tarif");
                o[2] = r.getString("harga");
                o[3] = r.getString("keterangan");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("terjadi kesalahan");
        }
    }

    private void cariData() {
        DefaultTableModel tabel = new DefaultTableModel() {
            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };

        tabel.addColumn("ID Tarif");
        tabel.addColumn("Nama Tarif");
        tabel.addColumn("Harga Tarif");
        tabel.addColumn("Keterangan");

        try {
            Connection c = Koneksi.configDB();
            String sql = "Select * from tbl_tarif_tambahan where id_tarif like '%" + txtCari.getText() + "%'or nama_tarif like '%" + txtCari.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                });
            }
            tblTarif.setModel(tabel);
            loadData();
        } catch (Exception e) {
            System.out.println("Cari Data Error");
        } finally {
        }
    }

